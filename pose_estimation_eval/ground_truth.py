"""Load and compare against the existing InstantReplay ground truth format.

The ground truth JSON files pair with ``.poses.json`` files generated by the
iOS ``PoseCaptureTests``.  This module reads both formats and provides
utilities for comparing model outputs against the Apple Vision baseline.

Ground truth schema (per ``README.md``)::

    {
      "approaches": [
        {
          "approachStart": 2.01,
          "steps": {
            "first":       {"timestamp": 2.50, "foot": "left"},
            "second":      {"timestamp": 2.70, "foot": "right"},
            "orientation": {"timestamp": 2.85, "foot": "left"},
            "plant":       {"timestamp": 2.95, "foot": "right"}
          },
          "takeoff": 3.03,
          "peak": 3.41,
          "landing": 3.8
        }
      ]
    }

Pose data schema (``.poses.json``)::

    {
      "videoInfo": {"filename": "…", "duration": 15.0, "frameRate": 15.0, "frameCount": 225},
      "frames": [
        {
          "timestamp": 0.0,
          "observations": [
            {
              "torsoCentroid": {"x": 0.5, "y": 0.6},
              "joints": {"nose": {"x": 0.5, "y": 0.3}, …}
            }
          ]
        }
      ]
    }
"""

from __future__ import annotations

import json
from dataclasses import dataclass, field
from pathlib import Path

# ---------------------------------------------------------------------------
# Ground truth types
# ---------------------------------------------------------------------------


@dataclass
class StepLabel:
    timestamp: float
    foot: str  # "left" or "right"


@dataclass
class ApproachLabel:
    approach_start: float
    takeoff: float
    peak: float
    landing: float
    steps: dict[str, StepLabel] | None = None  # keys: first, second, orientation, plant


@dataclass
class GroundTruth:
    approaches: list[ApproachLabel] = field(default_factory=list)


def load_ground_truth(path: str | Path) -> GroundTruth:
    """Load a ground truth JSON file."""
    with open(path) as f:
        data = json.load(f)

    approaches: list[ApproachLabel] = []
    for a in data.get("approaches", []):
        steps_data = a.get("steps")
        steps: dict[str, StepLabel] | None = None
        if steps_data:
            steps = {}
            for key in ("first", "second", "orientation", "plant"):
                if key in steps_data:
                    steps[key] = StepLabel(
                        timestamp=steps_data[key]["timestamp"],
                        foot=steps_data[key]["foot"],
                    )
        approaches.append(
            ApproachLabel(
                approach_start=a["approachStart"],
                takeoff=a["takeoff"],
                peak=a["peak"],
                landing=a["landing"],
                steps=steps,
            )
        )

    return GroundTruth(approaches=approaches)


# ---------------------------------------------------------------------------
# Captured pose data types
# ---------------------------------------------------------------------------


@dataclass
class CapturedPoint:
    x: float
    y: float


@dataclass
class CapturedObservation:
    torso_centroid: CapturedPoint
    joints: dict[str, CapturedPoint]


@dataclass
class CapturedFrame:
    timestamp: float
    observations: list[CapturedObservation]


@dataclass
class VideoInfo:
    filename: str
    duration: float
    frame_rate: float
    frame_count: int


@dataclass
class CapturedPoseData:
    video_info: VideoInfo
    frames: list[CapturedFrame]


def load_pose_data(path: str | Path) -> CapturedPoseData:
    """Load a ``.poses.json`` file produced by the iOS pose capture tests."""
    with open(path) as f:
        data = json.load(f)

    vi = data["videoInfo"]
    video_info = VideoInfo(
        filename=vi["filename"],
        duration=vi["duration"],
        frame_rate=vi["frameRate"],
        frame_count=vi["frameCount"],
    )

    frames: list[CapturedFrame] = []
    for frame_data in data.get("frames", []):
        observations: list[CapturedObservation] = []
        for obs in frame_data.get("observations", []):
            tc = obs["torsoCentroid"]
            joints = {
                name: CapturedPoint(x=pt["x"], y=pt["y"])
                for name, pt in obs.get("joints", {}).items()
            }
            observations.append(
                CapturedObservation(
                    torso_centroid=CapturedPoint(x=tc["x"], y=tc["y"]),
                    joints=joints,
                )
            )
        frames.append(CapturedFrame(timestamp=frame_data["timestamp"], observations=observations))

    return CapturedPoseData(video_info=video_info, frames=frames)


# ---------------------------------------------------------------------------
# Comparison utilities
# ---------------------------------------------------------------------------


@dataclass
class JointError:
    """Per-joint Euclidean distance between predicted and reference poses."""

    joint_name: str
    euclidean_distance: float  # In normalised image coords (0-1)
    predicted: tuple[float, float] | None
    reference: tuple[float, float] | None


@dataclass
class FrameComparisonResult:
    """Comparison result for a single frame."""

    timestamp: float
    joint_errors: list[JointError]
    mean_error: float
    matched_joints: int
    total_reference_joints: int


def compare_joints(
    predicted: dict[str, tuple[float, float]],
    reference: dict[str, tuple[float, float]],
) -> list[JointError]:
    """Compare predicted joint positions against reference positions.

    Both dicts map ``joint_name → (x, y)`` in normalised coordinates.
    Returns one :class:`JointError` per reference joint.
    """
    errors: list[JointError] = []
    for name, (rx, ry) in reference.items():
        pred = predicted.get(name)
        if pred is None:
            errors.append(JointError(name, float("inf"), None, (rx, ry)))
        else:
            px, py = pred
            dist = ((px - rx) ** 2 + (py - ry) ** 2) ** 0.5
            errors.append(JointError(name, dist, (px, py), (rx, ry)))
    return errors
